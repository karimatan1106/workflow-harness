#!/usr/bin/env node
/**
 * Generate TEST_MATRIX.md from @spec and @covers annotations in test files.
 * Usage: node scripts/generate-test-matrix.js
 */

const fs = require('fs');
const path = require('path');

const TEST_DIR = path.resolve(__dirname, '..', 'src', '__tests__');
const HOOKS_TEST_DIR = path.join(TEST_DIR, 'hooks');
const OUTPUT = path.resolve(__dirname, '..', 'TEST_MATRIX.md');

function extractAnnotations(filePath) {
  const content = fs.readFileSync(filePath, 'utf8');
  const specs = [];
  const covers = [];

  // Extract @spec lines from header comment block
  const specRegex = /@spec\s+(.+)/g;
  let m;
  while ((m = specRegex.exec(content)) !== null) {
    specs.push(m[1].trim());
  }

  // Extract @covers lines from header comment block
  const coversRegex = /@covers\s+(.+)/g;
  while ((m = coversRegex.exec(content)) !== null) {
    covers.push(m[1].trim());
  }

  // Count test cases
  const testCount = (content.match(/\bit\s*\(/g) || []).length;

  return { specs, covers, testCount };
}

function collectTestFiles(dir) {
  const files = [];
  if (!fs.existsSync(dir)) return files;
  for (const entry of fs.readdirSync(dir)) {
    const full = path.join(dir, entry);
    if (entry.endsWith('.test.ts')) {
      files.push(full);
    }
  }
  return files;
}

const allFiles = [
  ...collectTestFiles(TEST_DIR),
  ...collectTestFiles(HOOKS_TEST_DIR),
].sort();

const rows = [];
let totalTests = 0;
let annotated = 0;
let unannotated = 0;
const specMap = new Map(); // spec -> [files]
const coverMap = new Map(); // source -> [files]

for (const filePath of allFiles) {
  const rel = path.relative(path.resolve(__dirname, '..', 'src'), filePath).replace(/\\/g, '/');
  const { specs, covers, testCount } = extractAnnotations(filePath);
  totalTests += testCount;

  if (specs.length > 0 || covers.length > 0) {
    annotated++;
  } else {
    unannotated++;
  }

  for (const s of specs) {
    if (!specMap.has(s)) specMap.set(s, []);
    specMap.get(s).push(rel);
  }
  for (const c of covers) {
    if (!coverMap.has(c)) coverMap.set(c, []);
    coverMap.get(c).push(rel);
  }

  rows.push({
    file: rel,
    testCount,
    specs: specs.length > 0 ? specs.join(', ') : '-',
    covers: covers.length > 0 ? covers.join(', ') : '-',
  });
}

// Generate markdown
const lines = [];
lines.push('# Test Matrix');
lines.push('');
lines.push(`Auto-generated by \`scripts/generate-test-matrix.js\`. Do not edit manually.`);
lines.push('');
lines.push(`- Files: ${allFiles.length}`);
lines.push(`- Tests: ${totalTests}`);
lines.push(`- Annotated: ${annotated} / ${allFiles.length} (${Math.round(annotated / allFiles.length * 100)}%)`);
lines.push(`- Unannotated: ${unannotated}`);
lines.push('');

// Section 1: File list
lines.push('## Test Files');
lines.push('');
lines.push('| File | Tests | Spec | Covers |');
lines.push('|------|-------|------|--------|');
for (const r of rows) {
  lines.push(`| ${r.file} | ${r.testCount} | ${r.specs} | ${r.covers} |`);
}
lines.push('');

// Section 2: Spec -> Tests
lines.push('## Spec to Test Mapping');
lines.push('');
const sortedSpecs = [...specMap.keys()].sort();
if (sortedSpecs.length > 0) {
  lines.push('| Spec | Test Files |');
  lines.push('|------|-----------|');
  for (const s of sortedSpecs) {
    lines.push(`| ${s} | ${specMap.get(s).join(', ')} |`);
  }
} else {
  lines.push('No @spec annotations found.');
}
lines.push('');

// Section 3: Source -> Tests
lines.push('## Source Coverage');
lines.push('');
const sortedCovers = [...coverMap.keys()].sort();
if (sortedCovers.length > 0) {
  lines.push('| Source Module | Test Files |');
  lines.push('|--------------|-----------|');
  for (const c of sortedCovers) {
    lines.push(`| ${c} | ${coverMap.get(c).join(', ')} |`);
  }
} else {
  lines.push('No @covers annotations found.');
}
lines.push('');

fs.writeFileSync(OUTPUT, lines.join('\n'), 'utf8');
console.log(`Generated ${OUTPUT}`);
console.log(`  ${allFiles.length} files, ${totalTests} tests, ${annotated} annotated, ${unannotated} unannotated`);
