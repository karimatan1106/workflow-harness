#!/bin/bash
# Workflow Harness Setup — install hooks into consuming project
# Usage: bash workflow-harness/setup.sh
# (or bash <path-to-harness>/setup.sh)

set -e

# Detect harness directory (where this script lives)
HARNESS_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_DIR="$(pwd)"

echo "=== Workflow Harness Setup ==="
echo "Harness: $HARNESS_DIR"
echo "Project: $PROJECT_DIR"

# 1. Create .claude/hooks/ if needed
mkdir -p "$PROJECT_DIR/.claude/hooks"

# 2a. Create 3-layer guard wrapper
# NOTE: Unquoted EOF so $HARNESS_DIR is expanded at setup time (baked path).
#       Runtime variables use \$ to survive the heredoc expansion.
cat > "$PROJECT_DIR/.claude/hooks/pre-tool-3layer-guard.sh" << EOF
#!/bin/bash
# Wrapper — delegates to workflow-harness canonical hook
# Auto-generated by workflow-harness/setup.sh
# Harness installed at: $HARNESS_DIR

HOOK="$HARNESS_DIR/hooks/pre-tool-3layer-guard.sh"
if [ -f "\$HOOK" ]; then
  exec bash "\$HOOK"
fi

# Fallback: search common locations relative to project root
SCRIPT_DIR="\$(cd "\$(dirname "\$0")" && pwd)"
PROJECT_ROOT="\$(cd "\$SCRIPT_DIR/../.." && pwd)"
for D in "workflow-harness" ".claude/workflow-harness" "vendor/workflow-harness"; do
  F="\$PROJECT_ROOT/\$D/hooks/pre-tool-3layer-guard.sh"
  if [ -f "\$F" ]; then
    exec bash "\$F"
  fi
done

# Harness not found — allow (don't block if harness is missing)
exit 0
EOF
chmod +x "$PROJECT_DIR/.claude/hooks/pre-tool-3layer-guard.sh"

# 2b. Create coordinator-recorder wrapper
cat > "$PROJECT_DIR/.claude/hooks/coordinator-recorder.sh" << EOF
#!/bin/bash
# Wrapper — delegates to workflow-harness canonical hook
# Auto-generated by workflow-harness/setup.sh
# Harness installed at: $HARNESS_DIR

HOOK="$HARNESS_DIR/hooks/coordinator-recorder.sh"
if [ -f "\$HOOK" ]; then
  exec bash "\$HOOK"
fi

# Fallback: search common locations relative to project root
SCRIPT_DIR="\$(cd "\$(dirname "\$0")" && pwd)"
PROJECT_ROOT="\$(cd "\$SCRIPT_DIR/../.." && pwd)"
for D in "workflow-harness" ".claude/workflow-harness" "vendor/workflow-harness"; do
  F="\$PROJECT_ROOT/\$D/hooks/coordinator-recorder.sh"
  if [ -f "\$F" ]; then
    exec bash "\$F"
  fi
done

# Harness not found — allow (don't block if harness is missing)
exit 0
EOF
chmod +x "$PROJECT_DIR/.claude/hooks/coordinator-recorder.sh"

# 2c. Create coordinator-cleanup wrapper (PostToolUse — removes coordinator ID)
cat > "$PROJECT_DIR/.claude/hooks/coordinator-cleanup.sh" << EOF
#!/bin/bash
# Wrapper — delegates to workflow-harness canonical hook
# Auto-generated by workflow-harness/setup.sh
# Harness installed at: $HARNESS_DIR

HOOK="$HARNESS_DIR/hooks/coordinator-cleanup.sh"
if [ -f "\$HOOK" ]; then
  exec bash "\$HOOK"
fi

# Fallback: search common locations relative to project root
SCRIPT_DIR="\$(cd "\$(dirname "\$0")" && pwd)"
PROJECT_ROOT="\$(cd "\$SCRIPT_DIR/../.." && pwd)"
for D in "workflow-harness" ".claude/workflow-harness" "vendor/workflow-harness"; do
  F="\$PROJECT_ROOT/\$D/hooks/coordinator-cleanup.sh"
  if [ -f "\$F" ]; then
    exec bash "\$F"
  fi
done

# Harness not found — allow (don't block if harness is missing)
exit 0
EOF
chmod +x "$PROJECT_DIR/.claude/hooks/coordinator-cleanup.sh"

# 3. Update .claude/settings.json
SETTINGS="$PROJECT_DIR/.claude/settings.json"

if [ ! -f "$SETTINGS" ]; then
  # Create new settings with both hooks
  mkdir -p "$PROJECT_DIR/.claude"
  cat > "$SETTINGS" << 'JSON'
{
  "hooks": {
    "PreToolUse": [
      {
        "matcher": "",
        "hooks": [{"type": "command", "command": "bash .claude/hooks/pre-tool-3layer-guard.sh"}]
      },
      {
        "matcher": "Agent",
        "hooks": [{"type": "command", "command": "bash .claude/hooks/coordinator-recorder.sh"}]
      }
    ],
    "PostToolUse": [
      {
        "matcher": "Agent",
        "hooks": [{"type": "command", "command": "bash .claude/hooks/coordinator-cleanup.sh"}]
      }
    ]
  }
}
JSON
  echo "Created $SETTINGS with 3-layer guard + coordinator-recorder + coordinator-cleanup"
else
  # Auto-add hooks to existing settings.json using node
  node -e "
const fs = require('fs');
const path = process.argv[1];
const settings = JSON.parse(fs.readFileSync(path, 'utf8'));
if (!settings.hooks) settings.hooks = {};
if (!settings.hooks.PreToolUse) settings.hooks.PreToolUse = [];

// 3-layer guard
const guardExists = settings.hooks.PreToolUse.some(h =>
  h.hooks && h.hooks.some(hh => typeof hh === 'object' ? (hh.command && hh.command.includes('3layer-guard')) : (typeof hh === 'string' && hh.includes('3layer-guard')))
);
if (!guardExists) {
  settings.hooks.PreToolUse.push({matcher: '', hooks: [{type: 'command', command: 'bash .claude/hooks/pre-tool-3layer-guard.sh'}]});
  console.log('Added 3-layer guard hook');
} else {
  console.log('3-layer guard already registered');
}

// coordinator-recorder
const recorderExists = settings.hooks.PreToolUse.some(h =>
  h.hooks && h.hooks.some(hh => typeof hh === 'object' ? (hh.command && hh.command.includes('coordinator-recorder')) : (typeof hh === 'string' && hh.includes('coordinator-recorder')))
);
if (!recorderExists) {
  settings.hooks.PreToolUse.push({matcher: 'Agent', hooks: [{type: 'command', command: 'bash .claude/hooks/coordinator-recorder.sh'}]});
  console.log('Added coordinator-recorder hook');
} else {
  console.log('coordinator-recorder already registered');
}

// coordinator-cleanup (PostToolUse)
if (!settings.hooks.PostToolUse) settings.hooks.PostToolUse = [];
const cleanupExists = settings.hooks.PostToolUse.some(h =>
  h.hooks && h.hooks.some(hh => typeof hh === 'object' ? (hh.command && hh.command.includes('coordinator-cleanup')) : (typeof hh === 'string' && hh.includes('coordinator-cleanup')))
);
if (!cleanupExists) {
  settings.hooks.PostToolUse.push({matcher: 'Agent', hooks: [{type: 'command', command: 'bash .claude/hooks/coordinator-cleanup.sh'}]});
  console.log('Added coordinator-cleanup hook');
} else {
  console.log('coordinator-cleanup already registered');
}

fs.writeFileSync(path, JSON.stringify(settings, null, 2) + '\n');
" "$SETTINGS"
fi

# 4. Create .agent directory for coordinator-ids tracking
mkdir -p "$PROJECT_DIR/.agent"

echo ""
echo "=== Setup Complete ==="
echo "3-layer guard + coordinator-recorder + coordinator-cleanup installed."
echo "Orchestrator/Coordinator/Worker access control active."
